void	ft_rev_int_tab(int *tab, int size)
{
	int	b;
	int	e;
	int	x;

	b = 0;
	e = size - 1;
	while (b < e)
	{
		x = tab[b];
		tab[b] = tab[e];
		tab[e] = x;
		b++;
		e--;
	}
}

int	ft_str_is_lowercase(char *str)
{
	int	l;

	l = 0;
	if (str[l] == '\0')
	{
		return (1);
	}
	while (str[l] != '\0')
	{
		if ((str[l] >= 'a') && (str[l] <= 'z'))
			l++;
		else
			return (0);
	}
	return (1);
}

int	ft_str_is_numeric(char *str)
{
	int	l;

	l = 0;
	if (str[l] == '\0')
	{
		return (1);
	}
	while (str[l] != '\0')
	{
		if ((str[l] >= '0') && (str[l] <= '9'))
			l++;
		else
			return (0);
	}
	return (1);
}

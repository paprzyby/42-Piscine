int	ft_str_is_uppercase(char *str)
{
	int	l;

	l = 0;
	if (str[l] == '\0')
	{
		return (1);
	}
	while (str[l] != '\0')
	{
		if ((str[l] >= 'A') && (str[l] <= 'Z'))
			l++;
		else
			return (0);
	}
	return (1);
}

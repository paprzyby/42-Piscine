int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	l;

	l = 0;
	if (n == 0)
	{
		return (0);
	}
	while ((s1[l] != '\0' || s2[l] != '\0') && (l < n))
	{
		if (s1[l] != s2[l])
			return (s1[l] - s2[l]);
		l++;
	}
	return (0);
}

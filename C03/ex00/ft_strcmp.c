int	ft_strcmp(char *s1, char *s2)
{
	int	l;

	l = 0;
	while ((s1[l] == s2[l]) && ((s1[l] != '\0') || (s2[l] != '\0')))
	{
		l++;
	}
	return (s1[l] - s2[l]);
}
